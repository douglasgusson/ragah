    public void atualizarTabela() {
        tbCidades.setModel(new CidadeTableModel(this.listaCidades));
//        tbCidades.setDefaultRenderer(Object.class, new CidadeCellRenderer());
        ((AbstractTableModel) tbCidades.getModel()).fireTableDataChanged();
    }

    private void initialize() {

        atualizarTabela();
        eventoFechar();

        // Se teclarmos ESC nesta janela, ela irÃ¡ se fechar:  
        this.getRootPane().registerKeyboardAction((ActionEvent e) -> {
            FrmBuscaCidade.this.retornarCidade();
            FrmBuscaCidade.this.dispose();
        }, KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0),
                JComponent.WHEN_IN_FOCUSED_WINDOW);

    }

    private void eventoFechar() {
        addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent e) {
                retornarCidade();
            }
        });
    }

    private Boolean retornarCidade() {

        int row = this.tbCidades.getSelectedRow();

        if (row != (-1)) {

            CidadeTableModel cidadeTableModel = new CidadeTableModel(this.listaCidades);
            Cidade c = cidadeTableModel.get(row);            

            if (this.getParent() instanceof FrmRegistroFuncionario) {
                FrmRegistroFuncionario frmRegistroFuncionario = FrmRegistroFuncionario.getInstancia();
                System.out.println(frmRegistroFuncionario.toString());
                frmRegistroFuncionario.setCidade(c);
                this.dispose();
            }

            return true;
        }

        return false;
    }

